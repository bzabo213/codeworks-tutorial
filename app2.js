//  CONDITIONALS

let numOne = 10;
let numTwo = 20;

// if/else
if(numOne < numTwo){
    console.log("numOne is less than numTwo");
} else if(numOne > numTwo){
    console.log("numOne is greater than numTwo");
} else {
    console.log("numOne and numTwo are equal");
}


// switch
let letter = "n";

switch(letter){
    case "n":
        console.log("n");
        break;
    case "N":
        console.log("N");
    break;
    case "a":
        console.log("a");
    break;
}